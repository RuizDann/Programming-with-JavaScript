/*
Task 1
Write a 'for' loop that will count from 1 up to 5
output 'Counting completed!' when done
*/

for (var i = 1; i < 6; i++) {
    console.log(i);
    if (i == 5) {
        console.log('Counting completed!');
    }
}
console.log('Task 1 complete\n');

/*
Task 2
Write a 'for' loop that will count from 5 down to 1
output 'Counting completed!' when done
*/

for (var i = 5; i > 0; i--) {
    console.log(i);
    if (i == 1) {
        console.log('Counting completed!');
    }
}
console.log('Task 2 complete\n');